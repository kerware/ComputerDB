    @Test
    @Transactional
    void getAllCompaniesByComputerIsEqualToSomething() throws Exception {
        Computer computer;
        if (TestUtil.findAll(em, Computer.class).isEmpty()) {
            companyRepository.saveAndFlush(company);
            computer = ComputerResourceIT.createEntity(em);
        } else {
            computer = TestUtil.findAll(em, Computer.class).get(0);
        }
        em.persist(computer);
        em.flush();
        company.addComputer(computer);
        companyRepository.saveAndFlush(company);
        Long computerId = computer.getId();
        // Get all the companyList where computer equals to computerId
        defaultCompanyShouldBeFound("computerId.equals=" + computerId);

        // Get all the companyList where computer equals to (computerId + 1)
        defaultCompanyShouldNotBeFound("computerId.equals=" + (computerId + 1));
    }

    /**
     * Executes the search, and checks that the default entity is returned.
     */
    private void defaultCompanyShouldBeFound(String filter) throws Exception {
        restCompanyMockMvc
            .perform(get(ENTITY_API_URL + "?sort=id,desc&" + filter))
            .andExpect(status().isOk())
            .andExpect(content().contentType(MediaType.APPLICATION_JSON_VALUE))
            .andExpect(jsonPath("$.[*].id").value(hasItem(company.getId().intValue())))
            .andExpect(jsonPath("$.[*].name").value(hasItem(DEFAULT_NAME)));

        // Check, that the count call also returns 1
        restCompanyMockMvc
            .perform(get(ENTITY_API_URL + "/count?sort=id,desc&" + filter))
            .andExpect(status().isOk())
            .andExpect(content().contentType(MediaType.APPLICATION_JSON_VALUE))
            .andExpect(content().string("1"));
    }

    /**
     * Executes the search, and checks that the default entity is not returned.
     */
    private void defaultCompanyShouldNotBeFound(String filter) throws Exception {
        restCompanyMockMvc
            .perform(get(ENTITY_API_URL + "?sort=id,desc&" + filter))
            .andExpect(status().isOk())
            .andExpect(content().contentType(MediaType.APPLICATION_JSON_VALUE))
            .andExpect(jsonPath("$").isArray())
            .andExpect(jsonPath("$").isEmpty());

        // Check, that the count call also returns 0
        restCompanyMockMvc
            .perform(get(ENTITY_API_URL + "/count?sort=id,desc&" + filter))
            .andExpect(status().isOk())
            .andExpect(content().contentType(MediaType.APPLICATION_JSON_VALUE))
            .andExpect(content().string("0"));
    }

